
def find_element_index(array, value_to_find)
  counter = 0
  while counter < array.length do
    if array[counter] = value_to_find
      return counter
  counter += 1
end


def find_max_value(array)
  counter = 0
  while counter < array.length do
    if array[counter] > 4
      return counter
  counter += 1
end

def find_min_value(array)
  counter = 0
  while counter < array.length do
    if array[counter] = 1
      return counter
  counter += 1
end
